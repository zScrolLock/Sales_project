    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        List<Distribuidor> lista;
        DistribuidorControle dist_control = new DistribuidorControle();
        int codigo = Integer.parseInt(jTextField_busca_remov.getText());
        DefaultTableModel unique_dist = (DefaultTableModel) jTable2.getModel();
        
        if(!jTextField_busca_remov.getText().equals("") || jTextField_busca_remov.getText().equals(" ")){
            lista = dist_control.findById(codigo);
            if(!lista.isEmpty()){
                distribuidor = lista.get(lista.size()-1);
                unique_dist.addRow(new Object[]{distribuidor.getIdDistribuidor(), distribuidor.getNome(), distribuidor.getCidade(), distribuidor.getUf(), distribuidor.getTipo()});
                int result = JOptionPane.showConfirmDialog(null, "Deseja remover esse distribuidor?", "Sim ou não?", JOptionPane.YES_NO_OPTION);
                if(result == JOptionPane.YES_OPTION){
                    dist_control.remover(distribuidor);
                    JOptionPane.showMessageDialog(null, "Distribuidor removido com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    jTextField_busca_remov.setText("");
                    jTextField_busca_remov.requestFocus();
                    unique_dist.setRowCount(0);
                }else{
                    JOptionPane.showMessageDialog(null, "Não foi possivel remover distribuidor", "Alerta", JOptionPane.WARNING_MESSAGE);
                    jTextField_busca_remov.setText("");
                    jTextField_busca_remov.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Distribuidor não encontrado", "Alerta", JOptionPane.WARNING_MESSAGE); 
                jTextField_busca_remov.setText("");
                jTextField_busca_remov.requestFocus();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Campos incompletos", "Alerta", JOptionPane.WARNING_MESSAGE); 
        }
    }//GEN-LAST:event_jButton4ActionPerformed

