    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        List<Usuario> lista;
        String nome = jTextField_nome.getText();
        String cpf = jTextField_cpf.getText();
        String telefone = jTextField_telefone.getText();
        String login = jTextField_login.getText();
        String senha = jTextField_senha.getText();
        String aux_salario = jTextField_salario.getText();
        float salario;
            if(aux_salario.isEmpty()){
                salario = 0;
            }else{
                salario = Float.parseFloat(jTextField_salario.getText());
            }
        
        
        Usuario novoUser = new Usuario();
        UsuarioControle userControle = new UsuarioControle();
        
        if(!nome.equals("") || !cpf.equals("") || !telefone.equals("") || !login.equals("") || !senha.equals("")){
            if(!nome.equals(" ") || !cpf.equals(" ") || !telefone.equals(" ") || !login.equals(" ") || !senha.equals(" ")){
                novoUser.setNome(nome);
                novoUser.setCpf(cpf);
                novoUser.setTelefone(telefone);
                novoUser.setSalario(salario);
                novoUser.setLogin(login);
                novoUser.setSenha(senha);
               
                    if(jRadioButton_fun.isSelected()){
                        novoUser.setTipo(UserTipo.FUNCIONARIO);
                    }else if(jRadioButton_ger.isSelected()){
                        novoUser.setTipo(UserTipo.GERENTE);
                    }else{
                        JOptionPane.showMessageDialog(null, "Selecione o tipo de Funcionario", "Alerta", JOptionPane.WARNING_MESSAGE);
                    }
                    lista = userControle.findByCpf(cpf);
                        if(lista.isEmpty()){
                            userControle.inserir(novoUser);
                            JOptionPane.showMessageDialog(null, "Dados gravados com sucesso", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
                            jTextField_nome.setText("");
                            jTextField_cpf.setText("");
                            jTextField_telefone.setText("");
                            jTextField_salario.setText("");
                            jTextField_login.setText("");
                            jTextField_senha.setText("");
                            buttonGroup1.clearSelection();
                        }else{
                            JOptionPane.showMessageDialog(null, "CPF ja cadastrado", "Alerta", JOptionPane.WARNING_MESSAGE);
                            jTextField_cpf.setText("");
                            jTextField_cpf.requestFocus();
                        }               
            }else{
                JOptionPane.showMessageDialog(null, "Campos incompletos", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Campos incompletos", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        List<Usuario> user_remove;
        UsuarioControle user_controle = new UsuarioControle();
        String cpf_aux = jTextField_cpf_aux.getText();
        DefaultTableModel unique_user = (DefaultTableModel) jTable1.getModel();
        
        if(!cpf_aux.equals("") || !cpf_aux.equals(" ")){
            user_remove = user_controle.findByCpf(cpf_aux);
            if(!user_remove.isEmpty()){
                Usuario usuario = user_remove.get(user_remove.size()-1);
                unique_user.addRow(new Object[]{usuario.getNome(), usuario.getCpf(), usuario.getTelefone(), usuario.getTipo()});
                int result = JOptionPane.showConfirmDialog(null, "Deseja remover esse funcionario?", "Sim ou n√£o?", JOptionPane.YES_NO_OPTION);
                if(usuario.getCpf().equals(this.user.getCpf()) || usuario.getTipo().equals(this.user.getTipo().ADMINISTRADOR)){
                    JOptionPane.showMessageDialog(null, "N√£o √© possivel remover o proprio registro, ou super usuarios", "Alerta", JOptionPane.WARNING_MESSAGE);
                    jTextField_cpf_aux.setText("");
                    jTextField_cpf_aux.requestFocus();
                }else{
                    if(result == JOptionPane.YES_OPTION){
                        user_controle.remover(usuario);
                        JOptionPane.showMessageDialog(null, "Funcionario removido com sucesso", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
                        jTextField_cpf_aux.setText("");
                        jTextField_cpf_aux.requestFocus();
                        unique_user.setRowCount(0);
                    }else{
                        JOptionPane.showMessageDialog(null, "N√£o foi possivel remover o Funcionario", "Alerta", JOptionPane.WARNING_MESSAGE);
                        jTextField_cpf_aux.setText("");
                        jTextField_cpf_aux.requestFocus();
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null, "N√£o foi encontrado Funcionarios com esse CPF", "Alerta", JOptionPane.WARNING_MESSAGE);
                jTextField_cpf_aux.setText("");
                jTextField_cpf_aux.requestFocus();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Insira dados no campo de CPF do Funcionario", "Alerta", JOptionPane.WARNING_MESSAGE);
            jTextField_cpf_aux.setText("");
            jTextField_cpf_aux.requestFocus(); 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

