    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String nome = jTextField_newNome.getText();
        float preco = Float.parseFloat(jTextField_newPreco.getText());
        int quantidade = Integer.parseInt(jTextField_newQtd.getText());
        ProdutoControle prod_control = new ProdutoControle();
        
        if(!nome.equals("") || !jTextField_newPreco.getText().equals("") || !jTextField_newQtd.getText().equals("")){
            if(!nome.equals(" ") || !jTextField_newPreco.getText().equals(" ") || !jTextField_newQtd.getText().equals(" ")){
                this.produto.setNome(nome);
                this.produto.setPreco(preco);
                this.produto.setQuantidade(quantidade);
                if(jRadioButton_geral.isSelected()){
                    this.produto.setTipo(ProdutoTipo.GERAL);
                }else if(jRadioButton_ali.isSelected()){
                    this.produto.setTipo(ProdutoTipo.ALIMENTICIO);
                }else if(jRadioButton_emb.isSelected()){
                    this.produto.setTipo(ProdutoTipo.EMBALAGENS);
                }else{
                    JOptionPane.showMessageDialog(null, "Escolha o tipo do produto", "Alerta", JOptionPane.WARNING_MESSAGE);
                }
                prod_control.alterar(this.produto);
                jTextField_newNome.setText("");
                jTextField_newPreco.setText("");
                jTextField_newQtd.setText("");
                buttonGroup1.clearSelection();
                this.dispose();
                ViewProdutos tela_prod = new ViewProdutos(null, false, user, caixa);
                tela_prod.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Campos incompletos", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Campos incompletos", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        ProdutoControle prod_control = new ProdutoControle();
        
        int result = JOptionPane.showConfirmDialog(null, "Deseja remover esse Produto?", "Sim ou não?", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION){
                prod_control.remover(this.produto);
                JOptionPane.showMessageDialog(null, "Produto removido com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                ViewProdutos tela_produto = new ViewProdutos(null, false, user, caixa);
                tela_produto.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Não foi possivel remover o Produto", "Alerta", JOptionPane.WARNING_MESSAGE);
                jTextField1.setText("");
                jTextField1.requestFocus();
                DefaultTableModel produto_table = (DefaultTableModel) jTable1.getModel();
                produto_table.setRowCount(0);
            }
    }//GEN-LAST:event_jButton2ActionPerformed

