    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        List<Distribuidor> lista;
        String nome = jTextField_nome.getText();
        String telefone = jFormattedTextField3.getText();
        String uf = jTextField_uf.getText();
        String cidade = jTextField_cidade.getText();
        String endereco = jTextField_endereco.getText();
        int codigo = Integer.parseInt(jTextField_cod.getText());
        
        Distribuidor dist = new Distribuidor();
        DistribuidorControle dist_control = new DistribuidorControle();
        
        if(!nome.equals("") || !telefone.equals("") || !uf.equals("") || !cidade.equals("") || !endereco.equals("") || !jTextField_cod.getText().equals("")){
            if(!nome.equals(" ") || !telefone.equals(" ") || !uf.equals(" ") || !cidade.equals(" ") || !endereco.equals(" ") || !jTextField_cod.getText().equals(" ")){
                dist.setNome(nome);
                dist.setEndereco(endereco);
                dist.setIdDistribuidor(codigo);
                dist.setCidade(cidade);
                dist.setUf(uf);
                dist.setTelefone(telefone);
                
                if(jRadioButton_al.isSelected()){
                    dist.setTipo(DistribuidorTipo.ALIMENTICIO);
                }else if(jRadioButton_emb.isSelected()){
                    dist.setTipo(DistribuidorTipo.EMBALAGENS);
                }else if(jRadioButton_geral.isSelected()){
                    dist.setTipo(DistribuidorTipo.GERAL);
                }else{
                    JOptionPane.showMessageDialog(null, "Selecione o tipo do Distribuidor", "Alerta", JOptionPane.WARNING_MESSAGE);
                    buttonGroup1.clearSelection();
                }
                
                lista = dist_control.findById(codigo);
                if(lista.isEmpty()){
                    dist_control.inserir(dist);
                    jTextField_cidade.setText("");
                    jTextField_cod.setText("");
                    jTextField_endereco.setText("");
                    jTextField_nome.setText("");
                    jTextField_uf.setText("");
                    jFormattedTextField3.setText("");
                    buttonGroup1.clearSelection();
                    JOptionPane.showMessageDialog(null, "Distribuidor cadastrado com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);    
                }else{
                    JOptionPane.showMessageDialog(null, "Código de Distribuidor ja Existe", "Alerta", JOptionPane.WARNING_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Campos incompletos", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Campos incompletos", "Alerta", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

