    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(jTextField1.getText());
        
        ProdutoControle prod_control = new ProdutoControle();
        List<Produto> lista_produto = prod_control.findByCode(codigo);
        DefaultTableModel produto_table = (DefaultTableModel) jTable1.getModel();
        
        if(!lista_produto.isEmpty()){
            jPanel_table.setVisible(true);
            this.produto = lista_produto.get(lista_produto.size()-1);
            produto_table.addRow(new Object[]{this.produto.getIdProduto(), this.produto.getNome(), this.produto.getPreco(), this.produto.getQuantidade(), this.produto.getTipo()});
            jPanel_principal.setVisible(true);
            jTextField1.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Não foi encontrado produto com esse codigo", "Alerta", JOptionPane.WARNING_MESSAGE);
            jTextField1.setText("");
            jTextField1.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

